from pprint import pprint

x = TEST
x = PROD
x = x.strip()

# Data in differnt formats for quick access
text = x
lines = x.split("\n")
grid = [list(line) for line in lines]

result = 0

rows = len(grid)
cols = len(grid[0])

antinodes = set()
frequencies = {}

for i in range(rows):
    for j in range(cols):
        if grid[i][j] == ".":
            continue
        freq = grid[i][j]
        if freq not in frequencies:
            frequencies[freq] = []
        frequencies[freq].append((i, j))

pprint(frequencies)

for key in frequencies:
    for i in range(len(frequencies[key])):
        for j in range(i + 1, len(frequencies[key])):
            x1, y1 = frequencies[key][i]
            x2, y2 = frequencies[key][j]

            dx = x2 - x1
            dy = y2 - y1

            x3, y3 = x2, y2
            x4, y4 = x1, y1

            while True:
                added = False
                x3, y3 = x3 + dx, y3 + dy
                x4, y4 = x4 - dx, y4 - dy

                if 0 <= x3 < rows and 0 <= y3 < cols:
                    added = True
                    result += 1
                    antinodes.add((x3, y3))
                if 0 <= x4 < rows and 0 <= y4 < cols:
                    added = True
                    result += 1
                    antinodes.add((x4, y4))

                if not added:
                    break


for key in frequencies:
    for x, y in frequencies[key]:
        antinodes.add((x, y))


# make the grid again
new_grid = [["." for _ in range(cols)] for _ in range(rows)]

for x, y in antinodes:
    new_grid[x][y] = "#"

for key in frequencies:
    for x, y in frequencies[key]:
        new_grid[x][y] = key

for line in new_grid:
    print("".join(line))


